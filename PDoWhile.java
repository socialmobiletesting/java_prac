public class PDoWhile {
    public static void main(String args[])
    {
        int i = 5;
        do
        {
            System.out.println("Hello" + i);
            i++;

        }while(i<=4);
    }
}

// This do while loop will be used if I intentionally want to show one false value, here i value is 5 which is false as i<=4 defined
